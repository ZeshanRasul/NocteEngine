ComPtr<ID3DBlob> d3dUtil::CompileShader(
	const std::wstring& filename,
	const D3D_SHADER_MACRO* defines,
	const std::string& entrypoint,
	const std::string& target)
{
	UINT compileFlags = 0;
#if defined(DEBUG) || defined(_DEBUG)  
	compileFlags = D3DCOMPILE_DEBUG | D3DCOMPILE_SKIP_OPTIMIZATION;
#endif

	HRESULT hr = S_OK;

	ComPtr<ID3DBlob> byteCode = nullptr;
	ComPtr<ID3DBlob> errors;
	hr = D3DCompileFromFile(filename.c_str(), defines, D3D_COMPILE_STANDARD_FILE_INCLUDE,
		entrypoint.c_str(), target.c_str(), compileFlags, 0, &byteCode, &errors);

	if (errors != nullptr)
		OutputDebugStringA((char*)errors->GetBufferPointer());

	ThrowIfFailed(hr);

	return byteCode;
}

void d3dUtil::LoadObjModel(const std::string& filepath, Model& model)
{
	tinyobj::attrib_t attrib;
	std::vector<tinyobj::shape_t> shapes;
	std::vector<tinyobj::material_t> materials;
	std::string err;

	// Load the OBJ and MTL files
	if (!tinyobj::LoadObj(&attrib, &shapes, &materials, &err, filepath.c_str(), "materials\\"))
	{
		throw std::runtime_error(err);
	}

	int i = 0;
	for (auto& mat : materials)
	{
		auto material = new Material();
		material->Name = mat.name;
		material->DiffuseTextureFilePath = mat.diffuse_texname;
		material->DiffuseAlbedo = DirectX::XMFLOAT4(
			mat.diffuse[0],
			mat.diffuse[1],
			mat.diffuse[2],
			1.0f);
		material->FresnelR0 = DirectX::XMFLOAT3(
			mat.specular[0],
			mat.specular[1],
			mat.specular[2]);
		material->Roughness = mat.shininess / 256.0f;
		material->DiffuseSrvHeapIndex = 22;
		model.materials.push_back(material);
		auto texture = new Texture();
		texture->Name = mat.name;
		texture->Filename = AnsiToWString(mat.diffuse_texname);
		model.textures.push_back(texture);
		i++;
	}

	// Parse the model and store the unique vertices
	unordered_map<VertexObj, uint32_t> uniqueVertices = {};
	for (const auto& shape : shapes)
	{
		for (const auto& index : shape.mesh.indices)
		{
			VertexObj vertex = {};
			vertex.Pos =
			{
				attrib.vertices[3 * index.vertex_index + 2],
				attrib.vertices[3 * index.vertex_index + 1],
				attrib.vertices[3 * index.vertex_index + 0]
			};


			vertex.Normal =
			{
				attrib.normals[3 * index.normal_index + 2],
				attrib.normals[3 * index.normal_index + 1],
				attrib.normals[3 * index.normal_index + 0]
			};
			vertex.UV =
			{
				attrib.texcoords[2 * index.texcoord_index + 0],
				1 - attrib.texcoords[2 * index.texcoord_index + 1]
			};

			// Fast find unique vertices using a hash
			if (uniqueVertices.count(vertex) == 0)
			{
				uniqueVertices[vertex] = static_cast<uint32_t>(model.vertices.size());
				model.vertices.push_back(vertex);
			}

			model.indices.push_back(uniqueVertices[vertex]);
		}
	}
}

std::wstring DxException::ToString()const
{
	// Get the string description of the error code.
	_com_error err(ErrorCode);
	std::wstring msg = err.ErrorMessage();

	return FunctionName + L" failed in " + Filename + L"; line " + std::to_wstring(LineNumber) + L"; error: " + msg;
}


